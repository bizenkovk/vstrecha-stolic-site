function parseProgramBlocks(raw) {
  return raw
    .trim()
    .split(/\n\s*\n+/)
    .map((block) => {
      const [time = "", type = "", title = "", ...speakerLines] = block
        .split("\n")
        .map((line) => line.trim())
        .filter(Boolean);

      return {
        time,
        type: type === "-" ? "" : type,
        title,
        speaker: speakerLines.join(" ")
      };
    });
}

const COOKIE_CONSENT_KEY = "forum_cookie_consent";
const COOKIE_CONSENT_VALUE = "accepted";
const COOKIE_CONSENT_MAX_AGE = 60 * 60 * 24 * 180;

function readCookie(name) {
  return document.cookie
    .split(";")
    .map((chunk) => chunk.trim())
    .filter(Boolean)
    .find((chunk) => chunk.startsWith(`${name}=`))
    ?.slice(name.length + 1);
}

function persistCookieConsent() {
  const secureFlag = window.location.protocol === "https:" ? "; Secure" : "";
  document.cookie = `${COOKIE_CONSENT_KEY}=${COOKIE_CONSENT_VALUE}; Max-Age=${COOKIE_CONSENT_MAX_AGE}; Path=/; SameSite=Lax${secureFlag}`;

  try {
    window.localStorage.setItem(COOKIE_CONSENT_KEY, COOKIE_CONSENT_VALUE);
  } catch (error) {
    // Ignore storage restrictions and keep cookie-based fallback.
  }
}

function hasCookieConsent() {
  if (readCookie(COOKIE_CONSENT_KEY) === COOKIE_CONSENT_VALUE) {
    return true;
  }

  try {
    return window.localStorage.getItem(COOKIE_CONSENT_KEY) === COOKIE_CONSENT_VALUE;
  } catch (error) {
    return false;
  }
}

