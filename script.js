function escapeHtml(value) {
  return protectInitialsSpacing(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;");
}

function protectInitialsSpacing(value) {
  return value
    .replace(/([А-ЯЁA-Z][а-яёa-z]{0,2}\.)\s+([А-ЯЁA-Z][а-яёa-z]{0,2}\.)/g, "$1\u00A0$2")
    .replace(
      /([А-ЯЁA-Z][а-яёa-z-]+)\s+([А-ЯЁA-Z][а-яёa-z]{0,2}\.(?:[\u00A0 ]?[А-ЯЁA-Z][а-яёa-z]{0,2}\.)?)/g,
      "$1\u00A0$2"
    )
    .replace(
      /([А-ЯЁA-Z][а-яёa-z]{0,2}\.(?:[\u00A0 ]?[А-ЯЁA-Z][а-яёa-z]{0,2}\.)?)\s+([А-ЯЁA-Z][а-яёa-z-]+)/g,
      "$1\u00A0$2"
    );
}

function formatMultilineText(value) {
  return escapeHtml(value).replaceAll("\n", "<br>");
}

function formatProgramIntroText(value) {
  const normalizedValue = value.replace(
    /\s*(Сопредседатели:|Модераторы:|Секретариат:)/g,
    "\n$1"
  );

  return normalizedValue
    .trim()
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => `<span class="program-content__line">${escapeHtml(line)}</span>`)
    .join("");
}

function initNonBreakingInitials(root = document.body) {
  if (!root) return;

  const excludedTags = new Set(["SCRIPT", "STYLE", "NOSCRIPT", "TEXTAREA"]);
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;

      if (!parent || excludedTags.has(parent.tagName)) {
        return NodeFilter.FILTER_REJECT;
      }

      return /[А-ЯЁA-Z]\./.test(node.nodeValue)
        ? NodeFilter.FILTER_ACCEPT
        : NodeFilter.FILTER_SKIP;
    }
  });

  const textNodes = [];
  let currentNode = walker.nextNode();

  while (currentNode) {
    textNodes.push(currentNode);
    currentNode = walker.nextNode();
  }

  textNodes.forEach((node) => {
    const normalizedText = protectInitialsSpacing(node.nodeValue);

    if (normalizedText !== node.nodeValue) {
      node.nodeValue = normalizedText;
    }
  });
}

function parseCommitteeEntry(entry) {
  const normalized = entry.replace(/\s+/g, " ").trim();
  const delimited = normalized.split("|").map((item) => item.trim());

  if (delimited.length >= 2) {
    const [name, ...metaParts] = delimited;

    return {
      name,
      meta: metaParts.join(" | ")
    };
  }

  const parts = normalized.split(" ").filter(Boolean);
  const name = parts.slice(0, 3).join(" ");
  const meta = parts.slice(3).join(" ");

  return {
    name: name || normalized,
    meta
  };
}

